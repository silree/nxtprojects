	private final static long FRAME_TIME = 250;
	
	public static void main(String[] args) {
		DisplayUtil.drawImageCenter(ahh, 0, LCD.ROP_COPY); 
		final File aliveSound = new File("Alive.wav");
		final File deadSound = new File("Dead.wav"); 
		final File endSound = new File("End.wav"); 
		if (aliveSound.exists()) {
			SoundUtil.playSampleAsync(aliveSound, Sound.VOL_MAX, true); 
		} else {
			Sound.beepSequenceUp(); 
		}
		TouchSensor ts = new TouchSensor(touchPort); 
		LCD.refresh();
		motor.setSpeed(120); 
		motor.backward(); 
		int i = 0;
		Image[] as = {ahh, al, ahh, ar};
		long startTime = System.currentTimeMillis(); 
		while(!Button.ESCAPE.isPressed()) {
			if (System.currentTimeMillis() >= startTime + FRAME_TIME) {
				i++;
				if (i >= as.length) {
					i = 0;
				}
				startTime = System.currentTimeMillis();
				DisplayUtil.drawImageCenter(as[i], 0, LCD.ROP_COPY);
			}
		}
		motor.setSpeed(360); 
		motor.forward();
		DisplayUtil.drawImageCenter(dead, 0, LCD.ROP_COPY);
		LCD.refresh(); 
		if (deadSound.exists()) {
			SoundUtil.playSampleAsync(deadSound, Sound.VOL_MAX, true); 
		} else {
			Sound.beepSequence(); 
		}
		while(!ts.isPressed()); 
		motor.rotate(-10);
		if (endSound.exists()) {
			Sound.playSample(endSound, Sound.VOL_MAX); 
			Delay.msDelay(500); 
		}
		NXT.shutDown();
	}
